<h1><u>Exploding Rifa Caps</u></h1>

<p style="text-align: left; font-size: 1.2rem">
    Many old computers have filter capacitors made by RIFA. They are used to filter out interference from mains power. However, they aren't necessary for the computer to function, and they have a huge problem - they weren't designed to last this long, and the plastic casing cracks over time. This lets in moisture and causes the capacitor to leak and explode, letting out large clouds of very strong-smelling white smoke. So open up your power supplies and clip out
    those caps immediately! They don't need to be replaced but it is recommended to replace them just to be safe. <u>Remember to use caution working in power supplies!</u> <br /><br />
